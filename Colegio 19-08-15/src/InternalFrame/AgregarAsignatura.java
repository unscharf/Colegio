    public void CargarAsignaturas() {
    LimpiarTabla();
    DAO d= new DAO(sf);
    List<Asignatura> a= DAO.CargarAsignaturas();
    if(a.isEmpty()){ JOptionPane.showMessageDialog(this, "Lista vacia", "", 
                     JOptionPane.INFORMATION_MESSAGE);}
    else{
    for(Asignatura asign: a){
    dtm.addRow(new Object[]{asign.getIdAsignatura(), asign.getNombreAsignatura()});
    }//for
    }//if
    }//
   
    public void LimpiarTabla(){
    for(int i=dtm.getRowCount()-1;i>=0;i--){
     dtm.removeRow(i);
    }
    }

